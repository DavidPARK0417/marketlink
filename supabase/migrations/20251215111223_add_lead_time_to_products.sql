-- ============================================
-- products 테이블에 lead_time 필드 추가
-- ============================================
-- 
-- 납기 정보를 저장하기 위한 필드를 추가합니다.
-- 예: "익일배송", "2-3일", "2~3일"
-- ============================================

ALTER TABLE products
ADD COLUMN IF NOT EXISTS lead_time TEXT;

COMMENT ON COLUMN products.lead_time IS '납기 (예: "익일배송", "2-3일", "2~3일")';

